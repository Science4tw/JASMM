package jasmm.application.service;

public class MessageChangePassword {
	
	private String password;
	
	private String passwordToUpdate;

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getPasswordToUpdate() {
		return passwordToUpdate;
	}

	public void setPasswordToUpdate(String passwordToUpdate) {
		this.passwordToUpdate = passwordToUpdate;
	}

}
